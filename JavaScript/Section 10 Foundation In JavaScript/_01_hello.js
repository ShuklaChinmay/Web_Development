// Variables and Constants in JavaScript:
/*

const : The const keyword is used to declare a block-scoped constant in JavaScript. Constants declared with const cannot be re-assigned and must be initialized at the time of declaration.

const --> constant is a variable where duplication is not allowed and value cannot be changed.--> for constants decalaration.

variables are of 2 types : var and let

1. var : The var keyword is used to declare a variable in JavaScript. Variables declared with var are function-scoped and can be re-assigned.
Var does not know what is the scope, it does not work on the concept of scope.

2. let : The let keyword is used to declare a block-scoped variable in JavaScript. Variables declared with let can be re-assigned.

Note :- Prefer using let and const over var for better code readability and maintainability and because of the issue in block scope and functional scope.


*/