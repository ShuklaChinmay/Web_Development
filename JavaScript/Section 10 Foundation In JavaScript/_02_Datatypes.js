// Differnt datatypes in JavaScript are :

// 1. String : A string is a sequence of characters enclosed in single quotes (' '), double quotes (" "), or backticks (` `). It is used to represent text data.

let name = "John Doe"; 

// 2. Number : A number is a numeric data type that can represent both integer and floating-point values. It is used to perform mathematical operations.

let age = 30;    

// 3. Boolean : A boolean is a data type that can have only two values: true or false. It is used to represent logical values.

let isStudent = true;    

// 4. Null : Null is a special data type that represents the absence of any value. It is used to indicate that a variable has no value.

let emptyValue = null; 

// 5. Undefined : Undefined is a special data type that represents a variable that has been declared but has not been assigned a value. It is used to indicate that a variable is uninitialized.

let uninitializedValue; 

// 6. Object : An object is a complex data type that can store collections of data and more complex entities. It is used to represent real-world objects and their properties. (Non primitive datatype)

let person = { 
    name: "John Doe",
    age: 30,
    isStudent: true
};  

// 7. Symbol : A symbol is a unique and immutable data type that is often used to create unique identifiers for object properties. It is used to create unique keys for object properties.

let uniqueId = Symbol("id");    

// 8. BigInt : A BigInt is a numeric data type that can represent integers with arbitrary precision. It is used to represent large integers that cannot be represented by the Number data type.

let bigNumber = BigInt("123456789012345678901234567890");

